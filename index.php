		<h1>Data design of the Staples ordering subsystem</h1>
		<h2>Sharpies.....</h2>
		<h3>Persona</h3>
		<p>The Staples ordering subsystem allows customers to order
			office supplies over the web.  They do not provide
			delievery services, rather the customer is directed to pick
			up their order from a store location. It seems to be geared
			towards larger orders of bulk office supplies.  The persona
			this service is intended for would most likely be a small
			business owner or office manager, let's call him Bob, of a
			business not large enough to justify one of the offic supply
			delivery services,but large enough that an online order for
			store pick up would save them a considerable amount of time.
			By not having to hunt around the store, wait in lines,or
			perform any financial transactions, they can get back to the
			important work of writing on things with sharpies.
		</p>
		<h3>Use Case</h3>
		<p>Bob is the owner of a small business, it's a paper pushing kind
			of business, like medical billing, or tax returns. He has 10 other
			employees other than himself. He show's upon Monday morning and
			realizes that the office is low on lot's of supplies; envolpes,
			pens, printer paper, ink toner, staples (of course), and sharpies.
			He get's online from his office desktop and goes to Staples.com.
			from here he enter's the name of the supplies he wants in the search
			bar, and quickly addes the above supplies in the quantities he needs.
			He does this often, so he has a profile set up, and completes the
			financial transaction by using his saved credit card.  He then tells
			his secretary to pick them up on her lunch break.
		</p>
